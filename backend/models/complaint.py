"""
Pydantic models for the CFPB complaint narrative generation endpoint.

POST /api/complaints/generate accepts dispute summary fields and returns
a factual, first-person CFPB complaint narrative generated by Claude.
"""
from typing import Optional
from pydantic import BaseModel


class ComplaintRequest(BaseModel):
    bureau: str
    creditor_name: str
    original_dispute_reason: str
    bureau_outcome: str          # "verified" | "deleted" | "corrected" | "no_response"
    sent_date: str               # ISO 8601 date string
    bureau_response_date: Optional[str] = None
    escalation_summary: Optional[str] = None  # brief description of escalation letter if sent


class ComplaintResponse(BaseModel):
    narrative: str
